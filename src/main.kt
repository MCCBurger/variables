fun main() {
    //Numbers
    var myByte : Byte = 2
    var myShort : Short = 50
    var myInt : Int = 255
    var myLong : Long = 6422
    var myFloat = 1.25
    var myDouble = 4546.2345

    //Boolean
    var myBoolean = true;

